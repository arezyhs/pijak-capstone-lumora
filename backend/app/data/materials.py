def get_total_material_count() -> int:
    return sum(len(category["materials"]) for category in MATERIALS_DATA)


def get_tags_for_subject(subject: str) -> list[str]:
    normalized = subject.lower().replace("_", "-").replace(" ", "-")
    for category in MATERIALS_DATA:
        candidates = {
            category["id"].lower(),
            category["name"].lower().replace(" ", "-"),
            category["name"].lower(),
        }
        if normalized in candidates or subject in candidates:
            return list(category.get("tags", []))
    return [normalized]
